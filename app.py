def calculadora (numero1,  numero2 ,resultado):
    if resultado == 1:
        return numero1 + numero2
    elif resultado == 2:
        return numero1 + numero2
    elif resultado == 3:
        return numero1 * numero2
    elif resultado == 4:
        return numero1 / numero2
    else: 
        return  0
